            <h3>¿Que hacer en Pococi?</h3> <br>
            <p>Se desarrolla un sitio web con información turística para la Cámara de Comercio, Agroindustria, Turismo y Servicios de Pococí (CCATUSPO), la cual fue fundada el 14 de mayo del 2015. Esta Organización No Gubernamental (ONG) busca contribuir con el desarrollo integral del cantón de Pococí, a través de alianzas estratégicas entre diversas instituciones y nuestros asociados.<br>
            <br>Este proyecto consiste en la elaboración de un sitio web en su fase de prototipo para CCATUSPO, la cual va a realizar promoción de las actividades turísticas, recreativas y gastronómicas del cantón de Pococí por medio de información perteneciente a cada una de las zonas potenciales del cantón (Guápiles, Cariari, La Rita, Roxana, La Colonia, Colorado) que promueva el crecimiento de negocios turísticos, recreativos y de interés popular del cantón de Pococí como hospedajes, restaurantes y sodas, canchas sintéticas, piscinas, centros sociales, complejos turísticos, así como ríos y cataratas de la región.<br>
            <br>Como bien se conoce, el cantón de Pococí sirve como puente entre San José y Limón por lo cual el número de personas que transitan cerca del pueblo es muy elevado, sin embargo no conocen nada o lo suficiente del pueblo. A pesar de transitar tan cerca las personas no hacen un alto en el camino para comer, divertirse u hospedarse en Pococí, inclusive los mismos habitantes del cantón al no conocer la diversidad de oferta que brindan los diversos comercios planean sus viajes a otros lugares.<br>
            <br>Es necesario dar a conocer a Costa Rica y más aún al pueblo de Pococí todas las bellezas del cantón, así como la variedad de restaurantes, hoteles, centros turísticos, entre otros que se ofrecen. Esto se puede realizar mediante la creación de un sitio web que contemple la información de estos lugares, de esta forma las personas podrían acceder desde cualquier dispositivo móvil y observar lugares que les convengan en un momento determinado.<br>
            <br>El propósito de la Cámara (CCATUSPO) con la creación de este sitio web es dar a conocer  los atractivos turísticos y recreativos  del cantón de Pococí, con esto se fomenta que la población tanto del cantón como del país en general visite estos lugares, de modo que el dinero que invierten en esta actividad quede en Pococí, incentivando el desarrollo de los comercios del cantón y regiones cercanas, lo que a largo plazo puede generar mayores oportunidades de empleo en la zona.<br>
            <br>Además, el sitio web beneficiaría a muchos negocios que no tienen el dinero suficiente para invertir en la creación de un sitio web que los ayude a promocionarse en la zona, y para solucionar esta debilidad, muchos de estos negocios recurren a realizar páginas de Facebook, sin embargo algunas de ellas no incluyen información necesaria del negocio como ubicación o contactos o cuentan con información desactualizada, lo que dificulta la búsqueda y la visita a estos lugares.<br>
            <br>Cada vez son más las empresas  que deciden ofrecer sus servicios en  internet, esto fomentado por la cantidad de usuarios que posee este medio,  al realizar la página web se le estará brindando a las empresas y lugares de la zona la oportunidad de darse a conocer, teniendo una publicidad de bajo costo, donde los usuarios podrán acceder  a la información en  desde cualquier dispositivo cuando lo deseen, y de esta forma poderse contactar con los negocios o llegar a los lugares como ríos o cataratas.<br>
            <br>Algunas ventajas que pueden obtener con la publicidad en la página web son:<br><br>
                    1.	La amplia cobertura lo cual les permite llegar a más personas tanto dentro y fuera del país, lo cual brinda la oportunidad de llegar a una gran cantidad de público objetivo.<br>
                    2.	Se incurrirá en bajos costos, debido a que un anuncio o publicidad en esta página Web, será menos costoso que promocionarse en otros medios tradicionales.</p>
            
			<h4>Misión</h4>
			<p>"Constituir un sitio web que albergue información básica sobre aquellos lugares turísticos, recreativos y gastronómicos del cantón de Pococí, que esté al alcance de todos los habitantes del cantón y les sea de gran utilidad."</p>
			<h4>Visión</h4>
			<p>"Ser la principal página web con lugares turísticos, recreativos y gastronómicos del cantón de Pococí, que promueva el desarrollo y posicionamiento de estos negocios de Pococí."</p>
			<h4>Objetivos</h4><br>
			<h4>Objetivo General</h4>
			<p>"Desarrollar un sitio web que almacene información turística, recreativa y gastronómica de las diferentes comunidades del cantón de Pococí que incentive a la población a visitar e invertir en el cantón.</p> 
			<h4>Objetivos Específicos </h4>
			<p>1.	Promover el desarrollo del cantón de Pococí, a través del turismo local.<br>
			2.	Brindar a la población información veraz y concisa acerca de destinos turísticos, recreativos y gastronómicos  del cantón de Pococí.<br>
			3.	Brindar una herramienta publicitaria a los comercios que se dediquen a la actividad turística, recreativa y gastronómica del cantón que no cuentan con los recursos necesarios para implementar esta herramienta en su negocio.</p>

